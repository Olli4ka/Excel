import pandas as pd

data = {
    'Магазин': ['Магазин A', 'Магазин A', 'Магазин B', 'Магазин C', 'Магазин C', 'Магазин B', 'Магазин A'],
    'Категорія': ['Електроніка', 'Одяг', 'Одяг', 'Електроніка', 'Одяг', 'Електроніка', 'Одяг'],
    'Виручка': [200, 150, 300, 400, 200, 500, 100]
}
df = pd.DataFrame(data)

result = df.groupby(['Магазин', 'Категорія'])['Виручка'].agg(['sum', 'mean'])

result = result.rename(columns={
    'sum': 'Сумарна виручка',
    'mean': 'Середня виручка'
})

print(result)